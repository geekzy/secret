
<p align="center">Selamat datang di sistem Diagnosa Penyakit Infeksi pada Anak </p>
<p>Ada  8 menu utama yang dapat digunakan oleh pakar, yaitu:</p>
<ul>
  <li>
    Menu       penyakit.<br>
      Menu ini berfungsi untuk  mengelola (tambah, edit dan hapus) informasi tentang penyakit, yang berisi kode  penyakit, nama penyakit, definisi dan keterangan. </li>
  <li>menu       gejala<br>
  Menu ini berfungsi untuk  mengelola (tambah, edit dan hapus) informasi tentang gejala penyakit yang ada,  yang berisi kode gejala, nama gejala dan probabilitas. Probabilitas bernilai  antara (0 &ndash; 1) dengan penulisan &ldquo;,&rdquo; (koma) menggunakan &ldquo;.&rdquo; (titik).</li>
  <li>menu       penyebab<br>
  Menu ini berfungsi untuk  mengelola (tambah, edit dan hapus) informasi tentang penyebab penyakit, yang  berisi kode penyebab dan nama penyebab.</li>
  <li>menu       solusi<br>
  Menu ini berfungsi untuk  mengelola (tambah, edit dan hapus) informasi tentang solusi penyakit, yang  berisi kode solusi dan nama solusi.</li>
  <li>Menu       gejala penyakit<br>
  Menu ini berfungsi untuk  mengkaitkan hubungan antara penyakit dengan gejala yang ada.</li>
  <li>menu       penyebab penyakit<br>
  Menu ini berfungsi untuk  mengkaitkan hubungan antara penyakit dengan penyebabnya.</li>
  <li>menu       solusi penyakit<br>
  Menu ini berfungsi untuk  mengkaitkan hubungan antara penyakit dengan solusinya.</li>
  <li>menu       ganti password<br>
  menu ini berfungsi untuk  mengganti password admin.</li>
</ul>
