
<p>Aplikasi ini dibuat untuk memberikan pelayanan tentang diagnosis penyakit infeksi pada anak. menu yang kami sediakan adalah: </p>
<p>Home<br />
  Merupakan halaman utama<br />
  Informasi penyakit<br />
  Merupakan fasilitas untuk melihat penyakit-penyakit yang  telah dimasukkan oleh pakar, dilengkapi dengan fasilitas pencarian dengan  2kategori pencarian yaitu berdasarkan nama penyakit dan gejala penyakit<br />
  Penelusuran penyakit</p>
Merupakan fasilitas untuk mendiagnosis suatu penyakit  dengan cara mesukkan gejala-gejala yang dialami dan probabilitasnya (antara 0 -  1). Dibagian bawah dari form penelusuran terdapat informasi tentang gejala yang  telah dipilih, penyakit-penyakit yang terdeteksi dan perhitungan probabilitas  dengan menggunakan teorema Bayes.
